----------------------------
-- Creación de las tablas
----------------------------
CREATE TABLE GUIA_DETALLE (
    IdGuia               int NOT NULL,
    IdProducto           int NOT NULL,
    PrecioVenta          numeric(10,2) NOT NULL,
    Cantidad             int NOT NULL,
	CONSTRAINT PK_GUIA_DETALLE PRIMARY KEY (IdGuia, IdProducto)
);
go
 
 
CREATE TABLE ORDEN_DETALLE (
    IdOrden              int NOT NULL,
    IdProducto           int NOT NULL,
    PrecioCompra         numeric(10,2) NOT NULL,
    CantidadSolicitada   int NOT NULL,
    CantidadRecibida     int NULL,
    Estado               varchar(10) NULL,
	CONSTRAINT PK_ORDEN_DETALLE PRIMARY KEY (IdOrden, IdProducto)
);
go
  
 
CREATE TABLE PRODUCTO (
    IdProducto           int IDENTITY,
    IdCategoria          int NOT NULL,
    IdProveedor          int NOT NULL,
    Nombre               varchar(60) NOT NULL,
    UnidadMedida         varchar(30) NULL,
    PrecioProveedor      numeric(10,2) NULL,
    StockActual          int NULL,
    StockMinimo          int NULL,
    Descontinuado        int NOT NULL,
	CONSTRAINT PK_PRODUCTO PRIMARY KEY (IdProducto),
	CONSTRAINT U_PRODUCTO_NOMBRE UNIQUE (Nombre, UnidadMedida) 
);
go
 
 

CREATE TABLE PROVEEDOR (
        IdProveedor          int IDENTITY,
        Nombre               varchar(60) NOT NULL,
        Representante        varchar(30) NULL,
        Direccion            varchar(60) NULL,
        Ciudad               varchar(15) NULL,
        Departamento         varchar(15) NULL,
        CodigoPostal         varchar(15) NULL,
        Telefono             varchar(15) NULL,
        Fax                  varchar(15) NULL,
		CONSTRAINT PK_PROVEEDOR PRIMARY KEY (IdProveedor)
)
go
 

 
CREATE TABLE CATEGORIA (
    IdCategoria        int IDENTITY(1,1),
    Nombre		       varchar(20) NOT NULL,
    Descripcion        varchar(40) NULL,
	CONSTRAINT PK_CATEGORIA PRIMARY KEY (IdCategoria),
	CONSTRAINT U_CATEGORIA_NOMBRE UNIQUE (Nombre)
);
go
 

CREATE TABLE GUIA (
        IdGuia               int NOT NULL,
        IdLocal              int NOT NULL,
        FechaSalida          datetime NOT NULL,
        Transportista        varchar(30) NOT NULL,
		CONSTRAINT PK_GUIA PRIMARY KEY (IdGuia)
);
go


CREATE TABLE LOCAL (
    IdLocal              int NOT NULL,
    Direccion            varchar(60) NULL,
    Distrito             varchar(20) NULL,
    Telefono             varchar(15) NULL,
    Fax                  varchar(15) NULL,
	CONSTRAINT PK_LOCAL PRIMARY KEY (IdLocal)
);
go
 

CREATE TABLE ORDEN (
    IdOrden              int NOT NULL,
    FechaOrden           datetime NOT NULL,
    FechaEntrada         datetime NULL,
	CONSTRAINT PK_ORDEN PRIMARY KEY (IdOrden)
)
go
 

ALTER TABLE GUIA_DETALLE ADD CONSTRAINT FK_GUIA_DETALLE_PRODUCTO 
	FOREIGN KEY (IdProducto) REFERENCES PRODUCTO(IdProducto);
go
 
ALTER TABLE GUIA_DETALLE ADD CONSTRAINT FK_GUIA_DETALLE_GUIA
	FOREIGN KEY (IdGuia) REFERENCES GUIA(IdGuia);
go
  
ALTER TABLE ORDEN_DETALLE ADD CONSTRAINT FK_ORDEN_DETALLE_ORDEN
	FOREIGN KEY (IdOrden) REFERENCES ORDEN(IdOrden);
go
 
ALTER TABLE ORDEN_DETALLE ADD CONSTRAINT FK_ORDEN_DETALLE_PRODUCTO
    FOREIGN KEY (IdProducto) REFERENCES PRODUCTO(IdProducto);
go
  
ALTER TABLE PRODUCTO ADD CONSTRAINT FK_PRODUCTO_PROVEEDOR 
    FOREIGN KEY (IdProveedor) REFERENCES PROVEEDOR(IdProveedor)
go
 
ALTER TABLE PRODUCTO ADD CONSTRAINT FK_PRODUCTO_CATEGORIA
    FOREIGN KEY (IdCategoria) REFERENCES CATEGORIA(IdCategoria);
go
 
ALTER TABLE GUIA ADD CONSTRAINT FK_GUIA_LOCAL
    FOREIGN KEY (IdLocal) REFERENCES LOCAL(IdLocal);
go
 
SELECT name FROM sysobjects WHERE type = 'U'
go


